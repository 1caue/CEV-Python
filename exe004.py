print(f'\033[33mO tipo primitivo desse valor é\033[m {type(a)}')
print(f'\033[33mSó tem espaços?\033[m {a.isspace()}')
print(f'\033[33mÉ um número?\033[m {a.isnumeric()}')
print(f'\033[33mÉ alfabético?\033[m {a.isalpha()}')
print(f'\033[33mÉ alfanumérico?\033[m {a.isalnum()}')
print(f'\033[33mEstá em maiúsculas?\033[m {a.isupper()}')
print(f'\033[33mEstá em minúsculas?\033[m {a.islower()}')
print(f'\033[33mEstá capitalizado?\033[m {a.istitle()}')
